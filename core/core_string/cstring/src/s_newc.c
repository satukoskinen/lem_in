#include "../inc/cstr.h"

char	*s_newc(t_size size, int c)
{
	char		*out;
	t_size		i;

	out = s_new(size);
	i = 0;
	while (i < size)
	{
		out[i] = c;
		i++;
	}
	return (out);
}

/*
**  ----------------------------------------------------------------------------
**
**	S_new
**
**	String new; create an allocated string of size `size` format string with
**	`c`.
**
**  ----------------------------------------------------------------------------
*/
