#include "../inc/cstr.h"

void	s_clr(char *s)
{
	if (s)
		while (*s)
			*s++ = '\0';
}

/*
**  ----------------------------------------------------------------------------
**
**	S_clr
**
**	String clear; replaces all characters in `s` with a null terminating
**	character.
**
**  ----------------------------------------------------------------------------
*/
