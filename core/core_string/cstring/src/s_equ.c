#include "../inc/cstr.h"

int	s_equ(char const *s1, char const *s2)
{
	if (!s1 || !s2)
		return (0);
	if (s_len(s1) != s_len(s2))
		return (0);
	while (*s1 && *s2)
	{
		if (*s1 != *s2)
			return (0);
		s1++;
		s2++;
	}
	return (1);
}

/*
**  ----------------------------------------------------------------------------
**
**	S_equ
**
**	String equals; if the two strings are equal, returns 1. Otherwise
**	returns 0.
**
**  ----------------------------------------------------------------------------
*/
