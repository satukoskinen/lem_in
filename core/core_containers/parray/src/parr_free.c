#include "../../../inc/core.h"

t_ssize	parr_free(t_parray *arr)
{
	if (parr_null(arr))
		return (CR_FAIL);
	free(arr->data);
	*arr = (t_parray){NULL, 0, 0};
	return (CR_SUCCESS);
}

