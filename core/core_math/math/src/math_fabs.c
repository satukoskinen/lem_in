double	math_fabs(double n)
{
	if (n < 0)
		return (n * -1);
	else
		return (n);
}

/*
**  ----------------------------------------------------------------------------
**
**	math_fabs
**
**	Return the absolute (positive) value of a floating point number.
**
**  ----------------------------------------------------------------------------
*/
