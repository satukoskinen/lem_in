#include "../../../inc/core.h"

t_uint64	math_prime(t_uint64 n)
{
	t_uint64	i;

	i = 5;
	if (n <= 3)
		return (1);
	else if (n % 2 == 0 || n % 3 == 0)
		return (0);
	while (i * i <= n)
	{
		if (n % i == 0 || n % (i + 2) == 0)
			return (0);
		i = i + 6;
	}
	return (1);
}

/*
**  ----------------------------------------------------------------------------
**
**	math_prime
**
**	Calculate if `n` is a prime number and return 1 if so.
**
**  ----------------------------------------------------------------------------
*/
