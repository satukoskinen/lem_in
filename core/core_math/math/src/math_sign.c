#include "../../../inc/core.h"

int	math_sign(t_int64 n)
{
	if (n != 0)
	{
		if (n > 0)
			return (1);
		else
			return (-1);
	}
	else
		return (0);
}

/*
**  ----------------------------------------------------------------------------
**
**	math_sign
**
**	Return the sign of `n`. If `n` is zero returns 0.
**
**  ----------------------------------------------------------------------------
*/
