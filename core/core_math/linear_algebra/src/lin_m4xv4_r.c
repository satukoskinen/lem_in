#include "../../../inc/core.h"

t_vec4	lin_m4xv4_r(t_mat4 *a, t_vec4 *b)
{
	t_vec4	c;

	c.v[0] = a->m[0] * b->v[0] + a->m[1] * b->v[1]
		+ a->m[2] * b->v[2] + a->m[3] * b->v[3];
	c.v[1] = a->m[4] * b->v[0] + a->m[5] * b->v[1]
		+ a->m[6] * b->v[2] + a->m[7] * b->v[3];
	c.v[2] = a->m[8] * b->v[0] + a->m[9] * b->v[1]
		+ a->m[10] * b->v[2] + a->m[11] * b->v[3];
	c.v[3] = a->m[12] * b->v[0] + a->m[13] * b->v[1]
		+ a->m[14] * b->v[2] + a->m[15] * b->v[3];
	return (c);
}

