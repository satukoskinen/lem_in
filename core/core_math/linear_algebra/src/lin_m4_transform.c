#include "../../../inc/core.h"

t_mat4	lin_m4_transform(double scale)
{
	t_mat4	out;

	out = lin_m4_id();
	out.m[0] *= scale;
	out.m[5] *= scale;
	out.m[10] *= scale;
	out.m[15] *= scale;
	return (out);
}

