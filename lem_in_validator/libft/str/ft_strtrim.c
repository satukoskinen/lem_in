#include "libft.h"

char	*ft_strtrim(char const *s)
{
	t_size	start;
	t_size	end;

	if (s == NULL)
		return (NULL);
	start = 0;
	while (s[start] == ' ' || s[start] == '\n' || s[start] == '\t')
		start++;
	end = ft_strlen(s);
	while ((s[end] == '\0' || s[end] == ' '
			|| s[end] == '\n' || s[end] == '\t') && end != start)
		end--;
	return (ft_strsub(s, start, end - start + 1));
}
